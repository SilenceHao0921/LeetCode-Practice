#定義函式，名稱 reverse，輸入 x（整數），回傳 int。
#:int 和 -> int 都是型別提示，方便閱讀、輔助 IDE。
def rev(x:int)->int:
#記錄原始數字的正負號。
    sign = -1 if x < 0 else 1
#把 x 轉為正數，方便我們做數字反轉（不用處理負號的麻煩）。
    x = abs(x)
    
#初始化反轉後的數字，從 0 開始累積。
#反轉數字會用乘 10 + 取餘數的方式拼湊出來。
    r1 = 0
#迴圈條件：只要 x 還有數字（不是 0），就繼續反轉。每次迴圈處理一位數字。
    while x != 0 :
#反轉核心：
#x % 10：取出 x 的最後一位數。
#rev * 10：讓之前的反轉數字進位一格。
# * 10 + x % 10：把取出的數字加到反轉數字後面。
        r1 = r1 *10 + x % 10
#去掉最後一位數：
        x // 10
#恢復原本的正負號：
    r1 *= sign 
    
# 檢查是否超出 32 位元整數範圍
    if r1 < -2**31 or r1 > 2**31 - 1:
        return 0
    return rev